<?php

/*
    Proměnné slouží pro ukládání dat, se kterými v programu pracujeme.
*/

// Typy proměnných

// string (řetězec znaků)
$name = 'Bob';

// integer (celé číslo)
$age = 5;

// float (desetinné číslo)
$pi = 3.14;

// boolean (logická hodnota pravda/nepravda resp. ano/ne)
$locked = true;

// array (pole je složená proměnná, která obsahuje jiné proměnné)
$fruit = ['ananas', 'banana', 'orange'];

// null (speciální typ - hodnota není nastavena)
$empty = null;

// v PHP existují další typy proměnných, ale ty jsou mimo rozsah této přednášky.